package com.bailian.json;

public class JsonResult {
	
	private String opt_code;
	
	
	private String msg;
	
	private Object memberInfo;

	public String getOpt_code() {
		return opt_code;
	}

	public void setOpt_code(String opt_code) {
		this.opt_code = opt_code;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public Object getMemberInfo() {
		return memberInfo;
	}

	public void setMemberInfo(Object memberInfo) {
		this.memberInfo = memberInfo;
	}
	
	

}
